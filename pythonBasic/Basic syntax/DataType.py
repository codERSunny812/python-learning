# Demonstration of various data types in Python

print("Demonstrating different data types in Python:")


x= "sunny"
print("x:", x)
print("Type of x:", type(x))

x= 20
print("x:", x)
print("Type of x:", type(x))

x= 5.5
print("x:", x)
print("Type of x:", type(x))

x= True
print("x:", x)
print("Type of x:", type(x))

x= 3 + 4j
print("x:", x)
print("Type of x:", type(x))

x= [1, 2, 3, 4, 5]
print("x:", x)
print("Type of x:", type(x))

x = (1,2,3,4,5,6)
print("x:",x)
print("Type of x:", type(x))



x= { "name": "John", "age": 30 }
print("x:", x)
print("Type of x:", type(x))

x= {1, 2, 3, 4, 5}
print("x:", x)  
print("Type of x:", type(x))

x= frozenset([1, 2, 3, 4, 5])
print("x:", x)
print("Type of x:", type(x))

x= range(6)
print("x:", x)
print("Type of x:", type(x))

x= bytes(5)
print("x:", x)
print("Type of x:", type(x))

x= bytearray(5)
print("x:", x)
print("Type of x:", type(x))

x= memoryview(bytes(5))
print("x:", x)
print("Type of x:", type(x))


x= None
print("x:", x)
print("Type of x:", type(x))
