
# how to declare variables in python

# declaring a variable and assigning a value to it
x = 10
y = 5.5
name = "John Doe"
is_active = True
# printing the values of the variables
print("x:", x) 
print("y:", y)
print("name:", name)
print("is_active:", is_active)  

# changing the value of a variable
x = 20
print("Updated x:", x)  
# declaring multiple variables in one line
a, b, c = 1, 2.5, "Hello"
print("a:", a)
print("b:", b)
print("c:", c)
print("hello world")



# if you want to specify the data type of a variable, you can use constructors

age= int(30)
print("age:", age)